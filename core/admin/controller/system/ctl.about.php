    function cachemgr($c='',$f=''){
           if(defined('SAAS_MODE')&&SAAS_MODE){
            exit;
        }
        if(defined('WITHOUT_CACHE') && WITHOUT_CACHE){
            $this->pagedata['cache'] = false;
